-- Mahdi theta 컬럼 오버플로우 수정 (2026-07-21, 운영점검보고서 §2-1)
--
-- 2026-07-16 점검에서 "옵션 체인 적재 실패(NumericValueOutOfRange)"를 처음 발견했을 때는
-- 원인 불명이었다. 2026-07-19에 추가한 원시값 로깅(_raw_kis_output1) 덕분에 2026-07-21 로그에서
-- 근본원인을 확정: KIS가 돌려주는 theta는 원화 단위 하루 시간가치 감소분이라 얇은/근접만기
-- 옵션일수록 절댓값이 수백~수천대까지 커지는데(실측 최대 |theta|=9625.4268), theta 컬럼이
-- DECIMAL(8,6)(정수부 최대 2자리, 절댓값 99.999999까지만 허용)이라 상시 오버플로우한다.
--
-- 같은 로그로 delta_val(최대 |0.886|)·gama(최대 |0.0571|)·vega(최대 61.3252)는 전부 기존 컬럼
-- 범위 안에 들어옴을 확인했다 — theta만 단독으로 문제였다(전수 재검토 완료, 다른 컬럼은 안 건드림).
--
-- 위클리 옵션은 만기가 가까울수록 theta 절댓값이 커지므로, 이 버그는 만기 임박일수록 유실이
-- 심해지다가 만기 하루 전엔 사실상 전량 유실되는 형태로 나타났다(DB 실측: 15:45 시점 근접
-- 두 만기의 데이터가 통째로 사라지고 월물만 남음). NUMERIC(14,4)로 확장해 정수부 10자리까지
-- 여유를 둔다(gex 컬럼 DECIMAL(18,4)과 같은 접근, theta는 gex보다 자릿수가 작아 14자리로 충분).
--
-- NUMERIC/DECIMAL 간 정밀도 축소(6자리 → 4자리) 변경이라도 Postgres가 자동으로 반올림해
-- 재작성하므로 USING 절이 필요 없다. 같은 타입으로 재실행해도 에러 없이 통과해(재적용 안전),
-- 매 기동마다 전체 마이그레이션을 재실행하는 오늘자 재발방지 로직(scripts/start_mahdi_premarket.bat)과
-- 호환된다.
ALTER TABLE option_analysis_1m ALTER COLUMN theta TYPE NUMERIC(14,4);

COMMENT ON COLUMN option_analysis_1m.theta IS
    'KIS 원시값(원화 단위 하루 시간가치 감소분) 그대로 저장 — 정규화 안 됨. 2026-07-21 '
    'NUMERIC(8,6)→NUMERIC(14,4) 확장(실측 최대 절댓값 9625.4268로 기존 타입 상시 오버플로우, '
    '운영점검보고서 §2-1 참고).';
